package filter.java.pattern.example;
 
public class Employee {
 
       private String name;
       private String job;
       private String gender;
 
       public Employee(String name, String job, String gender){
          this.name = name;
          this.job = job;
          this.gender = gender;     
       }
 
       public String getName() {
          return name;
       }
        
       public String getJob() {
          return job;
       }
        
       public String getGender() {
              return gender;
       }
 
        
}